import Foundation
class CalculadoraModelo{
    func sumar(a:Double, b:Double)->Double{
        return a+b
    }
    func restar(a:Double, b:Double)-> Double{
        return a-b
    }
    func multiplicar(a:Double, b:Double)-> Double{
        return a*b
    }
    func dividir(a:Double, b:Double)-> Double{
        return a/b
    }
}
